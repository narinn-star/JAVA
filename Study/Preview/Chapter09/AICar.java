package chapter9;

public class AICar extends Car2 {

	@Override
	public void drive() {
		System.out.println("자율 주행");
		System.out.println("자동차가 스스로 방향전환");
	}

	@Override
	public void stop() {
		System.out.println("스스로 멈추기");
	}

	@Override
	public void wiper() {
		System.out.println("비나 눈의 양에 따라 빠르기가 자동으로 조절");
	}
	
}
